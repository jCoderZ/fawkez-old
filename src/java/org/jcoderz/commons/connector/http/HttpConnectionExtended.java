 package org.jcoderz.commons.connector.http;

/**
 * Interface extension of the HttpConnection for
 * retry handling. Implemented by the HttpConnectionImpl class
 * and used by the HttpConnectionFactoryImpl as handle for
 * a managed connection.
 *
 */
public interface HttpConnectionExtended
      extends HttpConnection
{
   /**
    * Gets flag indicating that a retry is required.
    *
    * @return boolean true if retry is required, false else
    */
   boolean isRetryRequired ();

   /**
    * Gets the amount of mili seconds to wait before retry.
    *
    * @return int amount of mili seconds for delay
    */
   int getRequiredDelayForRetries ();
}
